package com.example.sobirjonovproject.oop.triangle.mathfunction;

public class MathFunctions {
    public Double add(int a, int b){
        return (double)a+b;
    }
    public Double sub(int a, int b){
        return (double) a-b;
    }
    public Double multiply(int a, int b){
        return (double) a*b;
    }
    public Double div(int a, int b){
        return (double) a/b;
    }
    public Integer abc(Integer a){
        return Math.abs(a);
    }
    public Integer pow(Integer a){
        return a*a;
    }

}
